#include "libft.h"

int	ft_isspace(int s)
{
	unsigned char c;

	c = (unsigned char)s;
	if (c == '\t' || c == '\n' || c == '\v'
			|| c == '\f' || c == '\r' || c == ' ')
		return (1);
	else
		return (0);
}
